    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        try{
            int natid = Integer.parseInt(nationalid.getText());
            int phoneno = Integer.parseInt(phone.getText());
            stmt.executeUpdate("INSERT INTO getcleared ( surname, othername, national_id, offence, cell, sex) VALUES ('"+surname.getText()+"','"+othername.getText()+"','"+natid+"','"+offence.getText()+"','"+phoneno+"','"+gender.getSelectedItem()+"')");
            JOptionPane.showMessageDialog(GetclearedC.this, "Records Uploaded!");
            Statement stmtshow;
            stmtshow = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            rs=stmtshow.executeQuery("select cr_no from getcleared where national_id='"+natid+"'");
            rs.next();
            criminalno.setText(rs.getString("cr_no"));
            save.setEnabled(false);
            }
        catch(SQLException err){
            JOptionPane.showMessageDialog(GetclearedC.this, err.getMessage());
            System.out.println(err.getMessage());
        }
        catch(NumberFormatException ev){
            JOptionPane.showMessageDialog(GetclearedC.this, "Check the format of the ID No/Cell No entered");
            System.out.println(ev.getMessage());
        }
    }//GEN-LAST:event_saveActionPerformed

