    public void DoConnect(){
        try{
//STEP 2: Register the JDBC driver
            Class.forName("com.mysql.jdbc.Driver");//JDBC driver name
            
            //Database Credentials
            String host = "jdbc:mysql://localhost:3306/clearance"; //Database URL
            String uName = "root";
            String uPass = "";
            
//STEP 3: Open a connection
            conn = DriverManager.getConnection(host,uName,uPass);
            stmt = conn.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            
//STEP 4: Execute query
            String SQL = "Select * from getcleared";
            rs = stmt.executeQuery(SQL);
        
//Step 5: Extract data from the result set
            rs.next();
            surname.setText(rs.getString("surname"));
            othername.setText(rs.getString("othername"));
            nationalid.setText(Integer.toString(rs.getInt("national_id")));
            offence.setText(rs.getString("offence"));
            phone.setText(Integer.toString(rs.getInt("cell")));
            gender.setSelectedItem(rs.getString("sex"));
            criminalno.setText(rs.getString("cr_no"));
        }
        catch (SQLException | ClassNotFoundException err){
            JOptionPane.showMessageDialog(GetclearedC.this,err.getMessage());
        }
    }

